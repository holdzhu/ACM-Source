const int mod = 1000000007;

int solve(int n)
{
	long long s[81][81];
	long long g[81][81];
	long long tmp[81][81];
	memcpy(g, m, sizeof m);
	for (int i = 0; i < 81; ++i)
	{
		for (int j = 0; j < 81; ++j)
		{
			s[i][j] = i == j;
		}
	}
	while (n)
	{
		if (n & 1)
		{
			for (int i = 0; i < 81; ++i)
			{
				for (int j = 0; j < 81; ++j)
				{
					tmp[i][j] = 0;
					for (int k = 0; k < 81; ++k)
					{
						tmp[i][j] = (tmp[i][j] + s[i][k] * g[k][j]) % mod;
					}
				}
			}
			memcpy(s, tmp, sizeof s);
		}
		for (int i = 0; i < 81; ++i)
		{
			for (int j = 0; j < 81; ++j)
			{
				tmp[i][j] = 0;
				for (int k = 0; k < 81; ++k)
				{
					tmp[i][j] = (tmp[i][j] + g[i][k] * g[k][j]) % mod;
				}
			}
		}
		memcpy(g, tmp, sizeof g);
		n >>= 1;
	}
	long long rnt = 0;
	for (int a = 0; a < 3; ++a)
	{
		for (int b = 0; b < 3; ++b)
		{
			for (int c = 0; c < 3; ++c)
			{
				for (int d = 0; d < 3; ++d)
				{
					if (a && b && c && d)
					{
						rnt = (rnt + s[80][a * 27 + b * 9 + c * 3 + d]) % mod;
					}
				}
			}
		}
	}
	return rnt;
}

